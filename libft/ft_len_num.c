#include "libft.h"

int			len_num(char *t, char c)
{
	int		i;
	int		j;
	int		len;

	i = -1;
	while (t[++i])
	{
		if (t[i] != c && j == 0)
		{
			j = 1;
			len++;
		}
		if (t[i] == c)
			j = 0;
	}
	return (len);
}
